// Дано число. Ділить його на 2 стільки разів,
// поки результат поділу не стане меншим за 20. Виведіть це число.
// Порахуйте кількість ітерацій,
// і запишіть їх у зміну num.
// і якщо num % 3, виведіть "гарне число"
// інакше виведіть кількість ітерацій

let number = 123;
let num = 0;

while (number > 20) {
    number /= 2;
    num += 1;
}

if (num % 3 === 0) console.log("гарне число");
else console.log(`кількість ітерацій - ${num}`)





